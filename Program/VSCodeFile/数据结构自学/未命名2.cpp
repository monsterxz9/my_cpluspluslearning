
#include <stdio.h>

int main() {
    char T = '\007'; // 使用 char 来声明字符变量 T，并赋值为八进制数值 '\007'
    printf("%c\n", T);
    printf("Size of int: %zu bytes\n", sizeof(int));
    printf("Size of long: %zu bytes\n", sizeof(long));
    return 0;
}
 
